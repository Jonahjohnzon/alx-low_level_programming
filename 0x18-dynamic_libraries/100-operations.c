int add(int a, int b);
int sub(int a, int b);
int mul(int a, int b);
int div(int a, int b);
int mod(int a, int b);

/**
* add - addition
* @a: input
* @b: input
* Return: int
*/
int add(int a, int b)
{
	return (a + b);
}

/**
* sub - subtraction
* @a: input
* @b: input
* Return: int
*/
int sub(int a, int b)
{
	return (a - b);
}

/**
* mul - multiplication
* @a: input
* @b: input
* Return: int
*/
int mul(int a, int b)
{
	return (a * b);
}

/**
* div - division
* @a: input
* @b: input
* Return: int
*/
int div(int a, int b)
{
	return (a / b);
}

/**
  mod - modulus
  @a: input
  @b: input 
  Return: int
*/
int mod(int a, int b)
{
	return (a % b);
}
